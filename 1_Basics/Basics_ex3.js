'true'; // Type: String
console.log(typeof 'true');

false; // Type: Bool
console.log(typeof false);

1.5; // Type: Number (float)
console.log(typeof 1.5);

2; // Type: Number (int)
console.log(typeof 2);

undefined; // Type: Undefined
console.log(typeof undefined);

{
	foo: 'bar';
} // Object
console.log(typeof { foo: 'bar' });
